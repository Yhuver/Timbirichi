    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        String nombre=this.txtNombre.getText();
        
        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "Digite los datos correspondientes");
        }
        else{
            
        
            try {
                this.txtArea.setText("");
                String cad=this.myF.getMyC().registrarJugador(nombre, InetAddress.getLocalHost().toString().split("/")[1]);
                JOptionPane.showMessageDialog(null,cad);
                
                mostrarJugadoresConectados();
                
                this.txtNombre.setText("");
                this.txtNombre.setEnabled(false);
                this.btnCrear.setEnabled(false);
                this.btnConectar.setEnabled(false);
                
            } catch (Exception ex) {
                Logger.getLogger(PanelUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCrearActionPerformed

